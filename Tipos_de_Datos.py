'''Los datos en Python suelen ser guardados en distinto tipos
de variables
en general son 5 tipos de datos'''

'''numericos
string o cadena de texto
lista
diccionario
tuples'''

#Enteros
int_var = 12
print(type(int_var))
#Flotantes
float_var = 3.14
print(type(float_var))
#Cadena de texto o string
string_var = 'Visual-Izar'
print(type(string_var))
#lista
list_var = [1,2,'yo','tu','el']
print(type(list_var))
#diccionario
dicc_var = {1:'marcelo', 2:'viviana'}
print(type(dicc_var))
#tupla
tupla_var = (1,2,3,4,5,6,7,8,9)
print(type(tupla_var))

